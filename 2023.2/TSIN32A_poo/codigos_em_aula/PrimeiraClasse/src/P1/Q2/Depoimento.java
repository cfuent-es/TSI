package P1.Q2;

public class Depoimento {
	
	private boolean telefonou, esteveNoLocal, moraPerto, deviaDinheiro, trabalhou;

	public boolean isTelefonou() {
		return telefonou;
	}

	public void setTelefonou(boolean telefonou) {
		this.telefonou = telefonou;
	}

	public boolean isEsteveNoLocal() {
		return esteveNoLocal;
	}

	public void setEsteveNoLocal(boolean esteveNoLocal) {
		this.esteveNoLocal = esteveNoLocal;
	}

	public boolean isMoraPerto() {
		return moraPerto;
	}

	public void setMoraPerto(boolean moraPerto) {
		this.moraPerto = moraPerto;
	}

	public boolean isDeviaDinheiro() {
		return deviaDinheiro;
	}

	public void setDeviaDinheiro(boolean deviaDinheiro) {
		this.deviaDinheiro = deviaDinheiro;
	}

	public boolean isTrabalhou() {
		return trabalhou;
	}

	public void setTrabalhou(boolean trabalhou) {
		this.trabalhou = trabalhou;
	}
	
	

}
